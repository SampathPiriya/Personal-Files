				Jawaharlal Nehru Technological University, Kakinada (JNTU Kakinada) is a public (state government) university, located in Kakinada, East Godavari district, north of the Indian state of Andhra Pradesh. It is one of India's leading educational universities focusing on engineering. The university has been accredited by the National Board of Assessment and Accreditation Council (NAAC) of University Grants Commission (UGC) with 'A' grade. The university has been consistently ranked as the second best university in the state of Andhra Pradesh after Andhra University and as one of the premiere public technological universities across India.</p>

<p>JNTU Kakinada has three constituent colleges of Engineering, one at Kakinada and the other at Vijayanagaram and Guntur.</p>

<p>The College stood first in the state and fourteenth in the Country for the effective utilization of the funds under TEQIP phase-I. All the Engineering Departments of the College has been accredited by the National Board of Accreditation (NBA) of AICTE. The Tata Consultancy Services has accredited this College as a premier institution with A grade. The Wipro Ltd has accredited this college as Trusted Academic Partner.Several other companies like Honeywell, Infosys, Accenture, Larsen & Toubro, etc have also accredited this college.</p>



<p>JNTUK offers International Integrated Masters Degree Programme (IIMDP) of 5 years duration. B.Tech (Electronics and Communications Engineering) with Intake of 40 with 3-1/2 years course of study at JNTUK & 1-1/2 years course of study at Blekinge Institute of Technology, Sweden for M.Sc (Engineering) with Telecommunications Systems. B.Tech (Computer Science Engineering), Intake- 40, 3-1/2 years course of study at JNTUK & 1-1/2 years course of study at Blekinge Institute of Technology (BTH), Sweden for M.Sc (Engineering) with (a) Computer Science and (b) Software Engineering. B.Tech (Civil Engineering) – Intake- 60 – 3-1/2 years course of study at JNTUK & 1-1/2 years course of study at Asian Institute of Technology, Thailand for M.S with (a) Construction Engineering and Infrastructure Management, (b) Remote Sensing and Geographic Information Systems (c) Environmental Engineering and Management B.Tech (Electrical and Electronics Enguneering) with Intake- 40, 3-1/2 years course of study at JNTUK & 1-1/2 years course of study at Asian Institute of Technology (AIT), Bangkok, Thailand for M.S with (a) Energy Technology (b) Micro Electronics. B.Tech (Aviation Engineering) – 4 years duration – First 2 years at JNTUK, 1-1/2 years at U.S Flight Academy, United States with Associate of Applied Science (AAS) Professional Pilot Licenses and last 1/2 year at JNTUK.</p>


